#include <unistd.h>

static char	f(char c)
{
	if (c >= 'a' && c <= 'z')
		return (c - 'a' + 'A');
	if (c >= 'A' && c <= 'Z')
		return (c - 'A' + 'a');
	return (c);
}

int		main(int ac, char *av[])
{
	if (ac == 2)
	{
		av++;
		while (**av != '\0')
		{
			**av = f(**av);
			write(1, *av, 1);
			(*av)++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
