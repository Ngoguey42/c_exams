#include <unistd.h>

int	main(int ac, char *av[])
{
	int		t;
	int		j;

	if (ac == 2)
	{
		t = 1;
		j = 0;
		while (*av[1] != '\0')
		{
			if (*av[1] == ' ' || *av[1] == '\t')
				t = 0;
			else
			{
				if (t == 0 && j == 1)
					write(1, " ", 1);
				t = 1;
				j = 1;
				write(1, av[1], 1);
			}
			av[1]++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
