#include <unistd.h>

static int	match(char *s1, char *s2)
{
	while (*s1 != '\0')
	{
		if (*s2 == '\0')
			return (0);
		if (*s2 == *s1)
		{
			s1++;
			s2++;
		}
		else
			s2++;
	}
	return (1);
}

int			main(int ac, char *av[])
{
	if (ac != 3)
	{
		write(1, "\n", 1);
		return (0);
	}
	if (match(av[1], av[2]))
		write(1, "1", 1);
	else
		write(1, "0", 1);
	write(1, "\n", 1);
	return (0);
}
